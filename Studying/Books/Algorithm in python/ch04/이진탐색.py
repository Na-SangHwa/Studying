print("33순환탐색 ",binary_search(listA,33,0,len(listA)-1))
print("33반복탐색 ",binary_search_iter(listA,33,0,len(listA)-1))
print("32순환탐색 ",binary_search(listA,32,0,len(listA)-1))
print("32반복탐색 ",binary_search_iter(listA,32,0,len(listA)-1))